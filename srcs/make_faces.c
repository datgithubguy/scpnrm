#include "scop.h"

void	make_faces(float **faces, t_faces *f, t_vec3 *v, t_env *e)
{
	int	i[3];

	i[0] = -1;
	i[2] = 0;
	while (++i[0] < e->obj.raw_face_count)
	{
		i[1] = -1;
		while (++i[1] < 3)
			*(t_vec3*)((*faces) + ((i[0] + i[2]) * 3 * 3 + i[1] * 3)) =
			v[f[i[0]].indexes[i[1]] - 1];
		if (f[i[0]].count == 4)
		{
			i[1] = 0;
			*(t_vec3*)((*faces) + ((i[0] + 1 + i[2]) * 3 * 3)) =
			v[f[i[0]].indexes[i[1]] - 1];
			while (++i[1] < 3)
				*(t_vec3*)((*faces) + ((i[0] + 1 + i[2]) * 3 * 3 + i[1] * 3)) =
				v[f[i[0]].indexes[i[1] + 1] - 1];
			i[2]++;
		}
	}
}
